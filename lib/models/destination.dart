class Destination {
  final String id;
  final String city;
  final String country;
  final String image;
  final double rating;
  final int reviews;
  final double price;

  const Destination({
    required this.id,
    required this.city,
    required this.country,
    required this.image,
    required this.rating,
    required this.reviews,
    required this.price,
  });
}
